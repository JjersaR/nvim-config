return {
  "lewis6991/gitsigns.nvim",
  config = function()
    require('gitsigns').setup ({
      signs = {
        add          = { text = '┃' },
        change       = { text = '┃' },
        delete       = { text = '_' },
        topdelete    = { text = '‾' },
        changedelete = { text = '~' },
        untracked    = { text = '┆' },
      },
      signs_staged = {
        add          = { text = '┃' },
        change       = { text = '┃' },
        delete       = { text = '_' },
        topdelete    = { text = '‾' },
        changedelete = { text = '~' },
        untracked    = { text = '┆' },
      },
      signs_staged_enable = true,
      signcolumn = true,  -- Toggle with `:Gitsigns toggle_signs`
      numhl      = false, -- Toggle with `:Gitsigns toggle_numhl`
      linehl     = false, -- Toggle with `:Gitsigns toggle_linehl`
      word_diff  = false, -- Toggle with `:Gitsigns toggle_word_diff`
      watch_gitdir = {
        follow_files = true
      },
      auto_attach = true,
      attach_to_untracked = false,
      current_line_blame = false, -- Toggle with `:Gitsigns toggle_current_line_blame`
      current_line_blame_opts = {
        virt_text = true,
        virt_text_pos = 'eol', -- 'eol' | 'overlay' | 'right_align'
        delay = 1000,
        ignore_whitespace = false,
        virt_text_priority = 100,
        use_focus = true,
      },
      current_line_blame_formatter = '<author>, <author_time:%R> - <summary>',
      sign_priority = 6,
      update_debounce = 100,
      status_formatter = nil, -- Use default
      max_file_length = 40000, -- Disable if file is longer than this (in lines)
      preview_config = {
        -- Options passed to nvim_open_win
        style = 'minimal',
        relative = 'cursor',
        row = 0,
        col = 1
      },
      on_attach = function(bufnr)
        local gitsigns = require('gitsigns')

        local function map(mode, l, r, opts)
          opts = opts or {}
          opts.buffer = bufnr
          vim.keymap.set(mode, l, r, opts)
        end

        -- Navigation
        map('n', ']c', function()
          if vim.wo.diff then
            vim.cmd.normal({']c', bang = true})
          else
            gitsigns.nav_hunk('next')
          end
        end, { desc = "Next Git hunk" })

        map('n', '[c', function()
          if vim.wo.diff then
            vim.cmd.normal({'[c', bang = true})
          else
            gitsigns.nav_hunk('prev')
          end
        end, { desc = "Previous Git hunk" })

        -- Actions
        map('n', '<leader>gs', gitsigns.stage_hunk, { desc = "Stage hunk" })
        map('n', '<leader>gr', gitsigns.reset_hunk, { desc = "Reset hunk" })

        map('v', '<leader>gs', function()
          gitsigns.stage_hunk({ vim.fn.line('.'), vim.fn.line('v') })
        end, { desc = "Stage selected hunk" })

        map('v', '<leader>gr', function()
          gitsigns.reset_hunk({ vim.fn.line('.'), vim.fn.line('v') })
        end, { desc = "Reset selected hunk" })


        map("n", "<leader>gS", gitsigns.stage_buffer, { desc = "Stage entire buffer" })
        map("n", "<leader>gR", gitsigns.reset_buffer, { desc = "Reset entire buffer" })
        map("n", "<leader>gp", gitsigns.preview_hunk, { desc = "Preview hunk" })
        map("n", "<leader>gi", gitsigns.preview_hunk_inline, { desc = "Preview hunk inline" })

        map('n', '<leader>gb', function()
          gitsigns.blame_line({ full = true })
        end, { desc = "Show Git blame for line" })

        map('n', '<leader>gd', gitsigns.diffthis, { desc = "Diff current buffer" })

        map('n', '<leader>gD', function()
          gitsigns.diffthis('~')
        end, { desc = "Diff with previous commit" })

        map('n', '<leader>gQ', function() gitsigns.setqflist('all') end, { desc = "Populate quickfix with hunks" })
        map('n', '<leader>gq', gitsigns.setqflist, { desc = "Populate quickfix with all hunks" })

        -- Toggles
        map("n", "<leader>gb", gitsigns.toggle_current_line_blame, { desc = "Toggle line blame" })
        map("n", "<leader>gw", gitsigns.toggle_word_diff, { desc = "Toggle word diff view" })

        -- Text object
        map({ "o", "x" }, "<leader>gih", gitsigns.select_hunk, { desc = "Select hunk text object" })
      end
    })
  end
}
